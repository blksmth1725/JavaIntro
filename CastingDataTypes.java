public class CastingDataTypes {
  public static void main(String[] args) {
    // initial value
    int i = 200;

    // int to a short
    short s = (short) i;
    System.out.println(s);

    // int to a double
    double d = (double) i;
    System.out.println(d);

    // cast to a byte
    byte b = (byte) i;
    System.out.println(b);
  }
}
